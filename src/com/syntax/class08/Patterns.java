package com.syntax.class08;

public class Patterns {

	public static void main(String[] args) {

		/*  print this pattern 
		 * 
		 *  ****
		 *  ****
		 *  ****
		 *  ****
		 */
              
		
		for (int i = 1; i <= 4; i++) {

			for (int j = 1; j <= 4; j++) {
				System.out.print("*");
			}
          System.out.println();
		}
		System.out.println("-------------------------");
		
		
	}
}
