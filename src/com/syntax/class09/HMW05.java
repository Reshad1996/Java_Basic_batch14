package com.syntax.class09;

public class HMW05 {

	public static void main(String[] args) {
		
		//  Create an array to store char values and then print those in reverse order.
		
		char[] alph = {'A','B','C','D','E'};
		
		for (int i=alph.length-1 ; i>=0 ; i--) {
			
			System.out.print(alph[i]+" ");
		}

		System.out.println("-----------------------------------");
		
		// Another way
		
		
	}

}
