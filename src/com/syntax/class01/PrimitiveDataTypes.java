package com.syntax.class01;

public class PrimitiveDataTypes {

	public static void main(String[] args) {
		
        // storing and representing whole number values;
		byte box1 = 127;
		short box2 = 32767;
		int box3 = 100000000;  // popular 
		long box4 = 88555552288L;  // credit card // transation ID
		
		 // storing and representing decimal values;
		float pocket1 = 1.99f;
		double pocket2 = 99.99; // popular 
		 
		//storing single characters
		char container1 = 'A';
		char doller = '$';
		
		
		//storing yes or no values 
		boolean hot = true;
		boolean tired = false; 
		
		System.out.println(box1);
		System.out.println(box2);
		System.out.println(box3);
		System.out.println(box4);
		
		
		System.out.println(pocket1);  // printing value of the pocket1
		System.out.println("pocket1");  //printing text pocket1 
		System.out.println(pocket2);
		
		System.out.println(container1);
		System.out.println(doller);
		
		
		System.out.println(tired);
		System.out.println(hot);
		
		
	}

}
