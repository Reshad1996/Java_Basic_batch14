package com.syntax.class01;

public class VriableValue {

	public static void main(String[] args) {
	    
	                      // (DECLARING DATA)	 
		
		// 1: Numeric Primitive Data Type  
		// storing and representing primitive whole number 
		
		byte  cart1  = 123;
		short cart2  = 12345;
		int   cart3  = 123456789;
        long  cart4  = 12345678910111213L;
        
        // storing and representing primative decimel number 
        
        float   bank    = 2.66f;
        double  credit  = 77.55;
         
        // 2: Non Numeric Primitive Data Type 
        //        A: Character
        
        char jack  = 'M';
        char alina = 'F';
        char gmail = '@';
        char star  = '*';
        
        //        B: Boolean
        
        boolean cheater = false;
        boolean lover   = true;
        
        
                         // (PRINTING DATA)

        
        //(Printing Numeric values)
        System.out.println(cart1);
        System.out.println(cart2);
        System.out.println(cart3);
        System.out.println(cart4);
        
        
        //(Printing Decimal values)
        System.out.println(bank);
        System.out.println(credit);
        
        
        //(Printing Character values)
        System.out.println(jack);
        System.out.println(alina);
        System.out.println(gmail);
        System.out.println(star);
        
        
        //(Printing Boolean values)
        System.out.println(cheater);
        System.out.println(lover);

	}

}
